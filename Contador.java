package AulaJava17;


public class Contador {
public static void main(String[] args) {
	
	System.out.println("Contagem Crescente: ");
	for(int i=1; i <= 100 ; i++) {
		System.out.println(i);
	
	}
	System.out.println("Contagem decrescente: ");
	for(int i = 100; i >= 1 ; i--) {
		System.out.println(i);
	}
}
}

